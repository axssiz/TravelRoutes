export function getDistance(from, to) {
  const key = `${from}-${to}`;
  const reverseKey = `${to}-${from}`;
  return ROUTE_DISTANCES[key] || ROUTE_DISTANCES[reverseKey] || 800;
}

export function getDuration(distanceKm) {
  // 80 км/ч средняя скорость + перерывы
  const hours = Math.ceil(distanceKm / 80);
  return `${hours} часов`;
}
